
public class TestaVariaveis {
	public static void main(String[] args) {
		System.out.println("Olá, novo teste");
		
		// Criando uma variável tipo inteiro em Java.
		
		int age;
		age = 32;
		
		System.out.println(age);
		
		// Variáveis com Operadores.
		
		age = 10 + 12;
		
		System.out.println(age);
		
		age = (10 * 3) + 1;
		
		System.out.println("A idade é: " + age);
				
		
	}
}
